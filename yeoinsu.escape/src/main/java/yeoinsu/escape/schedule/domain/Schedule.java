package yeoinsu.escape.schedule.domain;

public class Schedule {
	private int scheNo;
	private String scheTime;
	private String scheState;
	private int scheLevel;
	private String scheTitle;
	
	public int getScheNo() {
		return scheNo;
	}
	
	public void setScheNo(int scheNo) {
		this.scheNo = scheNo;
	}
	
	public String getScheTime() {
		return scheTime;
	}
	
	public void setScheTime(String scheTime) {
		this.scheTime = scheTime;
	}
	
	public String getScheState() {
		return scheState;
	}
	
	public void setScheState(String scheState) {
		this.scheState = scheState;
	}
	
	public int getScheLevel() {
		return scheLevel;
	}
	
	public void setScheLevel(int sche_Level) {
		this.scheLevel = sche_Level;
	}
	
	public String getScheTitle() {
		return scheTitle;
	}
	
	public void setScheTitle(String shceTitle) {
		this.scheTitle = shceTitle;
	}
	
	
}
